package calculator

func Addition(numberA, numberB int) int {
	result := numberA + numberB
	return result
}

func Substraction(numberA, numberB int) int {
	result := numberA - numberB
	return result
}

func Multiplication(numberA, numberB int) int {
	result := numberA * numberB
	return result
}

func Division(numberA, numberB int) int {
	result := numberA / numberB
	return result
}
